const mongoose = require('mongoose');

const coursesSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  rating: {
    type: Number,
    default: 5,
  },
});

module.exports = mongoose.model( 'courses', coursesSchema );
